from typing import List
import pytest


class Solution:
    def numPairsDivisibleBy60(self, time: List[int]) -> int:
        """
        计算整除60的余数出现的次数，然后相乘
        """
        res = 0
        memo = dict()

        for t in time:
            key = t % 60
            memo[key] = memo.get(key, 0) + 1

        for remainder, count in memo.items():
            if remainder in (0, 30) and count > 1:
                res += (count*(count-1)//2)
            elif remainder not in (0, 30):
                res += (count * memo.get(60 - remainder, 0))
                # 加数计算了，则被加数不用再计算，故将其一count置为0
                memo[remainder] = 0
        return res


